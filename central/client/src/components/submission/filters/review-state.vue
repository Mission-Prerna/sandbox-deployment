<template>
  <label id="submission-filters-review-state" class="form-group">
    <select v-model="selectValue" class="form-control">
      <option value="">{{ $t('anyState') }}</option>
      <option v-for="reviewState of reviewStates" :key="reviewState"
        :value="odataLiteral(reviewState)">
        {{ $t(`reviewState.${reviewState}`) }}
      </option>
    </select>
    <span class="form-label">{{ $t('field.reviewState') }}</span>
  </label>
</template>

<script>
import useReviewState from '../../../composables/review-state';
import { odataLiteral } from '../../../util/odata';

export default {
  name: 'SubmissionFiltersReviewState',
  props: {
    // modelValue is an array because at one point, the filter was a
    // <select multiple>. However, the native <select multiple> was a confusing
    // user experience, so we made the filter a single-select. modelValue is
    // still an array because the filter will hopefully become a multiselect
    // again at some point (one with an improved user experience).
    modelValue: {
      type: Array,
      required: true
    }
  },
  emits: ['update:modelValue'],
  setup() {
    const { reviewStates } = useReviewState();
    return { reviewStates };
  },
  computed: {
    selectValue: {
      get() {
        return this.modelValue.length !== 0 ? this.modelValue[0] : '';
      },
      set(value) {
        this.$emit('update:modelValue', value !== '' ? [value] : []);
      }
    }
  },
  methods: {
    odataLiteral
  }
};
</script>

